package org.smile.commons;
/**
 * 预定义的一些字符
 * @author 胡真山
 *
 */
public interface Chars {
	char AMPERSAND        = '&';
	char AT               = '@';
	char ASTERISK         = '*';
	char BACK_SLASH       = '\\';
	char COLON            = ':';
	char COMMA            = ',';
	char DASH             = '-';
	char DOLLAR           = '$';
	/**点号*/
	char DOT              = '.';
	char EQUALS           = '=';
	char SLASH            = '/';
	char HASH             = '#';
	char HAT              = '^';
	char LEFT_BRACE       = '{';
	char LEFT_BRACKET     = '(';
	char LEFT_CHEV        = '<';
	char NEWLINE          = '\n';
	char N                = 'n';
	char PERCENT          = '%';
	char PIPE             = '|';
	char PLUS             = '+';
	char QUESTION_MARK    = '?';
	char EXCLAMATION_MARK = '!';
	/**双引号*/
	char QUOTE            = '"';
	char RETURN           = '\r';
	char TAB              = '\t';
	char RIGHT_BRACE      = '}';
	char RIGHT_BRACKET    = ')';
	char RIGHT_CHEV       = '>';
	char SEMICOLON        = ';';
	char SINGLE_QUOTE     = '\'';
	char BACKTICK         = '`';
	/**空格*/
	char SPACE            = ' ';
	char LEFT_SQ_BRACKET  = '[';
	char RIGHT_SQ_BRACKET = ']';
	/**下划线*/
	char UNDERSCORE       = '_';
	char Y                = 'y';
	/**1*/
	char ONE 			  = '1';
	/**0*/
	char ZERO			  = '0';
	/**分组符*/
	char GS				  =(char)29;
	/**记录分隔符*/
	char RS				  =(char)30;
	/**单元分隔符*/
	char US				  =(char)31;
	/**结束符*/
	char EOT			  =(char)4;
}
